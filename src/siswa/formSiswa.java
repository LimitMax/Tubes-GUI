 public void tampilSiswa(){
        Object[] row = {"NIS", "Nama Siswa", "Jenis kelamin","Tempat Lahir", "Tanggal", "Alamat", "Id Kelas"}; 
        DefaultTableModel tabmodel = new DefaultTableModel(null,row);
        tabelsiswa.setModel(tabmodel); 
        
        koneksi sambung = new koneksi();
        try{
            String sql = "SELECT nis,nama_siswa,jk,tempat_lahir,tanggal,alamat,id_kelas FROM siswa";
            ResultSet res = sambung.stat.executeQuery(sql);
            while(res.next()){
                String nis = res.getString("nis");
                String nama_siswa = res.getString("nama_siswa");
                String jk = res.getString("jk");
                String tempat_lahir = res.getString("tempat_lahir");
                Date tanggal = res.getDate("tanggal");
                String alamat = res.getString("alamat");
                String id_kelas = res.getString("id_kelas");
                Object[] data = {nis,nama_siswa,jk,tempat_lahir,tanggal,alamat,id_kelas};
                
                tabmodel.addRow(data);
            }
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println("Koneksi Gagal "+e.toString());
        }   
    }

